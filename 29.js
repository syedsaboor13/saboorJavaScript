// Function Expressions

// A function can also be stored in a variable


// Simple Function
function multiple(x, y){
    return x*y
}
let final = multiple(2 , 4);
console.log(final * 2);

// Fuction Expression
let multiple2 = function(x , y){
    return x*y
}
let final1= multiple2(2,5)
console.log(final1 * 6);