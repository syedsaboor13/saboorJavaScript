// Assignment Operator

// | Operator | Example |   Same As   |
// | -------- | ------- | -------     |
// | =        | x = 5   |  x = 5      |
// | +=       | x += 5  | x = x + 5   |
// | -=       | x -= 5  | x = x - 5   |
// | *=       | x *= 5  | x = x * 5   |
// | /=       | x /= 5  | x = x / 5   |
// | %=       | x %= 5  | x = x % 5   |
// | **=      | x **= 3 | x = x ** 3  |



let x = 5;
console.log(x += 12);
console.log(x -= 12);
console.log(x *= 12);
console.log(x /= 12);
console.log(x **= 12);
console.log(x %= 12);


